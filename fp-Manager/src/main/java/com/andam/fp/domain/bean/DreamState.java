package com.andam.fp.domain.bean;

public class DreamState {
    private Integer dreamStateId;

    private Integer dreamId;

    private Integer notSeeId;

    public Integer getDreamStateId() {
        return dreamStateId;
    }

    public void setDreamStateId(Integer dreamStateId) {
        this.dreamStateId = dreamStateId;
    }

    public Integer getDreamId() {
        return dreamId;
    }

    public void setDreamId(Integer dreamId) {
        this.dreamId = dreamId;
    }

    public Integer getNotSeeId() {
        return notSeeId;
    }

    public void setNotSeeId(Integer notSeeId) {
        this.notSeeId = notSeeId;
    }
}